public class Calculator {
    public double plus(int a, int b) {
        return (double) a+b;
    }
    public double umnozh(int a, int b) {
        return (double) a*b;
    }
    public double delen(int a, int b) {
        return (double) a/b;
    }
    public double minus(int a, int b) {
        return (double) a-b;
    }
}
